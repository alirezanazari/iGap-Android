package net.iGap.module.webrtc;

public class CallerInfo {
    public String name = "";
    public String lastName = "";
    public long userId;
    public String color = "";

    public long getUserId() {
        return userId;
    }

    public String getColor() {
        return color;
    }

    public String getLastName() {
        return lastName;
    }

    public String getName() {
        return name;
    }
}
